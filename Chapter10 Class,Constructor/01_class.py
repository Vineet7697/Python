class Employee:
    language="python" #This is a class attributes
    salary=1200000
    
vineet=Employee()
vineet.name="pavan" #This is an object attributes
print(vineet.name,vineet.salary,vineet.language)

rohan=Employee()
rohan.name="SHIVAM" #This is an object attributes
print(rohan.name,rohan.salary,rohan.language)


# Here name is object attributes and salary and language are class attributes as they directly belong to the class